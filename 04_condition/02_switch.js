// switch case is used fr to check multiple conditions
//one case and multiple values



let month = 11
switch (month) {
      case 1:
            console.log("january");
      break;
      case 2:
            console.log("february");
      break;
      case 3:
            console.log("march");
      break;
      case 4:
            console.log("april");
      break;
      case 5:
            console.log("may");
      break;

      default:console.log("not found");
      
            break;
}